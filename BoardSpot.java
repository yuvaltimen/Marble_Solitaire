package cs3500.marblesolitaire.model.hw02;

/**
 * Enumerates the possible positions on any board.
 *
 * <p>MARBLE - to represent the marble piece (O).
 * EMPTY - to represent a space where a marble can jump (_).
 * INVALID - an illegal spot, marble can't go here ( ).</p>
 */
public enum BoardSpot {
  MARBLE, EMPTY, INVALID
}
